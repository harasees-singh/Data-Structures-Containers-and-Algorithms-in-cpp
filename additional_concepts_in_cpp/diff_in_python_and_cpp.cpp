using namespace std;
int main(){
    // python is platform independent
    // cpp is platform dependent
    // python code is converted into 
    // an intermediate form (python byte code)
    // it is understood by python virtual machine
    // the final executable file is interpreted
    // cpp is directly compiled and therefore is faster
    // compilation phase and execution phase
    // python is dynamic typed language
    // cpp is a static typed language
    // that is an explicit declaration is required 
    // for every identifier
    short a; // generally 16bit integer
    long b = 23L;  // generally 64bit integer 
    unsigned ui=-1;
    cout << ui << endl;
}